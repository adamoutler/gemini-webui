document.addEventListener("DOMContentLoaded", () => {
  let modalMouseDownTarget = null;
  const settingsModal = document.getElementById("settings-modal");
  const quickAddModal = document.getElementById("quick-add-key-modal");
  const shareModal = document.getElementById("share-modal");
  const previewModal = document.getElementById("preview-modal");
  const automationModal = document.getElementById("automation-modal");

  [
    settingsModal,
    quickAddModal,
    shareModal,
    previewModal,
    automationModal,
  ].forEach((modal) => {
    if (!modal) return;
    modal.addEventListener("mousedown", (e) => {
      modalMouseDownTarget = e.target;
    });
    modal.addEventListener("mouseup", (e) => {
      if (modalMouseDownTarget === modal && e.target === modal) {
        if (
          modal === settingsModal &&
          typeof globalThis.closeSettings === "function"
        )
          globalThis.closeSettings();
        else if (
          modal === quickAddModal &&
          typeof globalThis.closeQuickAddKey === "function"
        )
          globalThis.closeQuickAddKey();
        else if (modal === shareModal) closeShareModal();
        else if (
          modal === previewModal &&
          typeof globalThis.closePreviewModal === "function"
        )
          globalThis.closePreviewModal();
        else if (
          modal === automationModal &&
          typeof globalThis.closeAutomationDashboard === "function"
        )
          globalThis.closeAutomationDashboard();
      }
      modalMouseDownTarget = null;
    });
  });
});
